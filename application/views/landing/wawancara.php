                                    <img src="<?= base_url('assets/img/logo-long.png') ?>" alt="Wawancara" class="img-fluid rounded mb-4" style="max-height:260px;">
                                    <p>Dalam dunia jurnalisme, wawancara menempati posisi yang sangat penting. Di balik setiap berita yang akurat dan menarik, selalu ada proses wawancara yang dilakukan dengan cermat. Tanpa wawancara yang baik, bahkan penulis terbaik pun akan kesulitan menghasilkan tulisan yang bernilai.</p>
                                    <p>Menurut <b>KBBI</b>, wawancara adalah tanya jawab antara pewawancara dan narasumber untuk memperoleh keterangan atau pendapat mengenai suatu hal. Dalam praktik jurnalistik, wawancara menjadi alat utama untuk menggali informasi langsung dari sumber yang relevan, baik itu pejabat, saksi, atau ahli di bidang tertentu.</p>
                                    <hr>
                                    <h2 class="mt-4">Mengapa Wawancara Menjadi Jantung Jurnalisme</h2>
                                    <p>Jurnalisme bukan sekadar menyampaikan informasi, tetapi menyajikan kebenaran yang telah diverifikasi. Wawancara membantu wartawan mendapatkan data primer yang belum tentu tersedia di sumber lain. Dari hasil wawancara inilah wartawan dapat menulis berita yang akurat, berimbang, dan dapat dipercaya publik.</p>
                                    <p>Kualitas wawancara menentukan mutu berita. Ketika wawancara dilakukan dengan teknik yang baik, hasilnya akan memperkaya sudut pandang berita sekaligus memperkuat kredibilitas media.</p>
                                    <hr>
                                    <h2 class="mt-4">Jenis-Jenis Wawancara</h2>
                                    <ul>
                                        <li><b>Wawancara Terstruktur (Terpimpin):</b> Wawancara ini berpedoman pada daftar pertanyaan yang sudah disiapkan sebelumnya. Pola ini membantu pewawancara tetap fokus dan memastikan seluruh informasi penting tidak terlewat.</li>
                                        <li><b>Wawancara Tidak Terstruktur (Bebas):</b> Jenis ini berlangsung lebih fleksibel dan cenderung santai. Pewawancara dapat menyesuaikan arah percakapan berdasarkan situasi di lapangan, asalkan tetap relevan dengan data yang dibutuhkan.</li>
                                    </ul>
                                    <hr>
                                    <h2 class="mt-4">Memahami Siapa Itu Narasumber</h2>
                                    <ul>
                                        <li><b>Saksi mata:</b> orang yang melihat atau mengalami langsung suatu peristiwa.</li>
                                        <li><b>Pejabat atau pihak berwenang:</b> yang memiliki tanggung jawab atau informasi resmi.</li>
                                        <li><b>Pakar atau ahli:</b> yang dapat memberikan penjelasan mendalam mengenai suatu isu.</li>
                                    </ul>
                                    <p>Apapun sumbernya, wartawan tetap wajib melakukan <b>verifikasi</b> atas semua informasi yang diterima. Verifikasi bisa dilakukan dengan membandingkan keterangan antar narasumber, atau melalui data pendukung dari dokumen, arsip, maupun literatur.</p>
                                    <hr>
                                    <h2 class="mt-4">Tahapan Wawancara yang Efektif</h2>
                                    <ol>
                                        <li>Menentukan topik atau masalah yang akan dibahas.</li>
                                        <li>Memahami topik secara mendalam agar wawancara tidak berlangsung “kosong”.</li>
                                        <li>Menyiapkan daftar pertanyaan.</li>
                                        <li>Menentukan narasumber yang tepat.</li>
                                        <li>Membuat janji dan mengatur waktu wawancara.</li>
                                    </ol>
                                    <p>Perencanaan yang matang membuat pewawancara lebih percaya diri dan mampu mengarahkan percakapan agar tetap fokus pada tujuan liputan.</p>
                                    <hr>
                                    <h2 class="mt-4">Bentuk-Bentuk Wawancara dalam Praktik Lapangan</h2>
                                    <ul>
                                        <li><b>Wawancara dengan Perjanjian:</b> dilakukan setelah menjadwalkan waktu terlebih dahulu dengan narasumber.</li>
                                        <li><b>Doorstepping:</b> pewawancara langsung mendatangi narasumber tanpa janji sebelumnya, biasanya di tempat umum.</li>
                                        <li><b>Wawancara di Tempat Kejadian (On the Spot Interview):</b> dilakukan langsung di lokasi peristiwa seperti kecelakaan, bencana, atau aksi massa.</li>
                                        <li><b>Konferensi Pers:</b> melibatkan banyak wartawan secara bersamaan, di mana semua media menerima informasi yang sama.</li>
                                    </ul>
                                    <p>Setiap jenis wawancara memerlukan pendekatan berbeda agar hasilnya optimal.</p>
                                    <hr>
                                    <h2 class="mt-4">Gaya Pewawancara: Temukan Karakter Sendiri</h2>
                                    <ul>
                                        <li><b>Tanya jawab sopan</b> – gaya formal dengan nada tenang dan terukur.</li>
                                        <li><b>Diskusi santai</b> – pewawancara dan narasumber terlibat dalam obrolan ringan namun tetap informatif.</li>
                                        <li><b>Pasif</b> – pewawancara mendengarkan lebih banyak dan membiarkan narasumber berbicara panjang.</li>
                                        <li><b>Agresif atau interogatif</b> – pewawancara aktif bertanya, menekan narasumber, dan menggali informasi secara mendalam.</li>
                                    </ul>
                                    <p>Pemilihan gaya wawancara bergantung pada topik, karakter narasumber, serta tujuan liputan.</p>
                                    <hr>
                                    <h2 class="mt-4">Etika dan Hak Narasumber</h2>
                                    <p>Dalam setiap wawancara, pewawancara wajib menjaga etika profesional. Beberapa hal yang perlu diperhatikan antara lain memperkenalkan identitas diri, menjelaskan tujuan wawancara, menghormati waktu narasumber, dan tidak memaksa menjawab pertanyaan yang dirasa sensitif.</p>
                                    <p>Kode Etik Jurnalistik juga menegaskan hak-hak narasumber, antara lain:</p>
                                    <ul>
                                        <li>Hak anonim untuk melindungi keselamatan pribadi.</li>
                                        <li>Hak mengetahui siapa pewawancara dan narasumber lain.</li>
                                        <li>Hak menentukan waktu wawancara.</li>
                                        <li>Hak menolak wawancara jika merasa tidak siap atau tidak diundang.</li>
                                    </ul>
                                    <p>Kepatuhan terhadap etika ini mencerminkan profesionalitas pewawancara sekaligus menjaga kepercayaan publik terhadap media.</p>
                                    <hr>
                                    <h2 class="mt-4">Atribusi dan Kerahasiaan Informasi</h2>
                                    <ul>
                                        <li><b>On the Record:</b> semua pernyataan boleh dikutip beserta identitas narasumber.</li>
                                        <li><b>On Background:</b> isi pernyataan boleh dikutip, tetapi identitas tidak disebutkan.</li>
                                        <li><b>On Deep Background:</b> informasi boleh digunakan tanpa dikutip langsung dan tanpa identitas.</li>
                                        <li><b>Off the Record:</b> informasi hanya untuk pengetahuan wartawan dan tidak boleh disiarkan.</li>
                                    </ul>
                                    <p>Kepatuhan terhadap kesepakatan atribusi ini menunjukkan integritas jurnalis dan menjaga hubungan baik antara wartawan dan narasumber.</p>
                                    <hr>
                                    <h2 class="mt-4">Tips Wawancara Sukses</h2>
                                    <ul>
                                        <li>Ciptakan suasana santai agar narasumber nyaman.</li>
                                        <li>Ajukan pertanyaan yang terbuka dan mendorong narasumber bercerita.</li>
                                        <li>Dengarkan dengan penuh perhatian.</li>
                                        <li>Jaga kontak mata dan hindari menyela pembicaraan.</li>
                                        <li>Ajukan pertanyaan susulan jika ada bagian yang belum jelas.</li>
                                        <li>Jangan berdebat dengan narasumber, tetapi gali informasi dengan cerdas.</li>
                                    </ul>
                                    <p>Wawancara yang baik tidak hanya soal bertanya, tetapi juga tentang kemampuan mendengarkan dan memahami jawaban dengan cermat.</p>
                                    <hr>
                                    <h2 class="mt-4">Penutup</h2>
                                    <p>Wawancara adalah seni menggali fakta dan emosi dalam percakapan. Wartawan tidak hanya berperan sebagai penanya, tetapi juga sebagai pendengar yang peka dan penulis yang jujur. Melalui wawancara yang dilakukan dengan etika dan profesionalitas, berita yang disajikan tidak sekadar informatif, tetapi juga mencerminkan integritas dan empati kepada publik.</p>
