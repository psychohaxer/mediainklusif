                                    <img src="<?= base_url('assets/img/logo-long.png') ?>" alt="Jurnalisme" class="img-fluid rounded mb-4" style="max-height:260px;">
                                    <p>Jurnalisme bukan sekadar pekerjaan menulis berita. Ia adalah seni, keterampilan, dan tanggung jawab moral untuk menyampaikan kebenaran kepada publik. Dalam pandangan <b>Adinegoro</b>, jurnalisme adalah kepandaian dalam menyusun kata yang bertujuan memberi kabar kepada masyarakat secepat mungkin dan seluas mungkin. Sedangkan menurut <b>Ridwan</b>, jurnalisme tidak hanya keterampilan teknis, tetapi juga sebuah bentuk seni dalam mengumpulkan dan mengedit berita agar layak disiarkan.</p>
                                    <p>Dua pandangan ini menegaskan satu hal: <b>jurnalisme adalah jantung dari arus informasi masyarakat</b>. Tanpa jurnalisme, kabar kehilangan arah dan kejelasan, sementara masyarakat kehilangan pijakan untuk memahami realitas.</p>
                                    <hr>
                                    <h2 class="mt-4">Sikap Seorang Jurnalis: Skeptis, Aktif, dan Siap Berubah</h2>
                                    <p>Jurnalisme hidup dari rasa ingin tahu. Seorang jurnalis tidak boleh mudah percaya pada informasi yang diterima. <b>Sikap skeptis</b> adalah fondasi pertama. Rasa ragu mendorong wartawan untuk bertanya, mencari, dan membuktikan hingga menemukan kebenaran yang bisa dipertanggungjawabkan.</p>
                                    <p>Namun, jurnalisme tidak berhenti pada keraguan. Wartawan juga dituntut untuk bertindak. Peristiwa tidak menunggu di ruang redaksi; mereka terjadi di lapangan. Wartawan sejati harus hadir di sana, menjadi saksi pertama, melihat dan mendengar langsung apa yang terjadi.</p>
                                    <p>Dunia jurnalisme juga menuntut kemampuan beradaptasi. Seperti kata filsuf Yunani <b>Heraclitus</b>, tidak ada yang tetap selain perubahan. Begitu pula dengan jurnalisme yang terus berkembang mengikuti dinamika zaman — dari media cetak, radio, televisi, hingga platform digital dan media sosial.</p>
                                    <hr>
                                    <h2 class="mt-4">Empat Elemen Penting dalam Dunia Pemberitaan</h2>
                                    <p>Abad ke-21 membawa perubahan besar dalam dunia jurnalisme. Hubungan satu arah antara wartawan dan pembaca kini telah berubah menjadi komunikasi dua arah. <b>Empat elemen utama</b> — wartawan, pesan, media, dan audiens — kini saling berinteraksi.</p>
                                    <ul>
                                        <li><b>Wartawan</b> masa kini tidak lagi bekerja secara individual, tetapi secara kolaboratif. Mereka harus terbuka, toleran, dan mampu bekerja dalam tim.</li>
                                        <li>Bentuk <b>pesan</b> yang disampaikan pun berubah; wartawan harus kreatif dalam menyajikan berita yang tidak hanya informatif, tetapi juga relevan dan bermakna.</li>
                                        <li><b>Media</b> juga bertransformasi menjadi multimedia. Sebuah berita kini bisa hadir di berbagai platform — situs web, radio, televisi, maupun media sosial.</li>
                                        <li><b>Audiens</b> tidak lagi hanya menjadi pembaca pasif; mereka turut menjadi produsen berita dengan berpartisipasi dalam penyebaran dan verifikasi informasi.</li>
                                    </ul>
                                    <hr>
                                    <h2 class="mt-4">Sepuluh Prinsip Jurnalisme yang Menjaga Marwah Profesi</h2>
                                    <p><b>Bill Kovach</b> dan <b>Tom Rosenstiel</b> dalam <i>The Elements of Journalism</i> menyebut bahwa jurnalisme memiliki prinsip-prinsip dasar yang tidak boleh diabaikan. Dalam bahan ini disebutkan sepuluh prinsip utama:</p>
                                    <ol>
                                        <li>Mengejar kebenaran.</li>
                                        <li>Memihak pada kepentingan publik.</li>
                                        <li>Menjalankan disiplin verifikasi.</li>
                                        <li>Menjaga kebebasan dalam peliputan.</li>
                                        <li>Bertugas sebagai pengawas independen terhadap kekuasaan.</li>
                                        <li>Menyediakan ruang bagi kritik dan komentar publik.</li>
                                        <li>Menjadikan hal penting tetap menarik dan relevan.</li>
                                        <li>Menyajikan berita secara proporsional dan menyeluruh.</li>
                                        <li>Menjaga etika dalam setiap langkah kerja.</li>
                                        <li>Memegang hak dan tanggung jawab profesional.</li>
                                    </ol>
                                    <blockquote class="blockquote my-4 p-3 bg-white border-start border-3 border-primary">
                                        <p class="mb-1">Mary Mapes pernah berkata, “Jurnalisme bukanlah obat, tetapi dapat menyembuhkan. Bukan hukum, tetapi dapat membawa keadilan. Bukan militer, tetapi dapat menjaga kita tetap aman.”</p>
                                        <footer class="blockquote-footer mt-2">Mary Mapes</footer>
                                    </blockquote>
                                    <p>Kalimat ini menggambarkan betapa besar peran jurnalisme dalam menjaga kebenaran dan keadilan sosial.</p>
                                    <hr>
                                    <h2 class="mt-4">Nilai-Nilai Utama: Akurasi dan Kecepatan</h2>
                                    <p>Dalam jurnalisme, kecepatan memang penting, tetapi <b>akurasi jauh lebih berharga</b>. Wartawan wajib memastikan setiap informasi yang disampaikan benar adanya, bukan sekadar cepat tersiar. Berita yang tidak akurat dapat menimbulkan kesalahpahaman, bahkan berujung pada kehilangan kepercayaan publik.</p>
                                    <p>Akurasi bukan hanya standar kerja, tetapi nilai dasar yang melekat pada etika jurnalis. Media yang akurat membangun kredibilitas, sedangkan media yang sering keliru akan kehilangan wibawa. Kecepatan harus berjalan beriringan dengan ketelitian agar berita yang disampaikan tidak menyesatkan.</p>
                                    <hr>
                                    <h2 class="mt-4">Bekal dan Syarat Seorang Wartawan</h2>
                                    <p>Menjadi wartawan bukan sekadar memiliki kemampuan menulis. Ia harus memiliki <i>nose for news</i> — naluri berita yang tajam untuk membedakan peristiwa biasa dengan peristiwa bernilai berita. Kemampuan observasi juga penting, karena dari pengamatan yang cermat wartawan dapat menemukan fakta tersembunyi.</p>
                                    <p>Selain rasa ingin tahu yang tinggi, wartawan juga harus memiliki kepribadian yang luwes, cepat dalam mengambil keputusan, dan mampu menyampaikan informasi dengan bahasa yang jelas serta mudah dipahami pembaca. Observasi dapat dilakukan dengan berbagai cara, baik partisipatif, nonpartisipatif, maupun secara diam-diam, tergantung pada situasi dan kebutuhan peliputan.</p>
                                    <hr>
                                    <h2 class="mt-4">Menulis Berita: Antara Ketepatan dan Kejelasan</h2>
                                    <p><b>Robert Gunning</b>, seorang konsultan media, merumuskan sepuluh prinsip menulis yang masih relevan hingga kini. Tulislah dengan kalimat yang singkat, jelas, dan sederhana. Pilih kata yang umum digunakan, hindari kata yang tidak perlu, dan berikan kekuatan pada kata kerja.</p>
                                    <ul>
                                        <li>Tulisan yang baik adalah tulisan yang terasa alami ketika dibaca — seolah pembaca sedang mendengarkan penuturnya berbicara.</li>
                                        <li>Kalimat yang sederhana justru membuat pesan lebih kuat dan mudah diingat.</li>
                                    </ul>
                                    <hr>
                                    <h2 class="mt-4">Menjaga Kemuliaan Profesi Jurnalis</h2>
                                    <p>Jurnalisme memiliki tujuan mulia: menjaga kejujuran, memastikan masyarakat mendapat informasi yang benar, dan melindungi nilai-nilai demokrasi. Tugas menulis berita bukan sekadar mencari sensasi, tetapi menjaga kepercayaan publik terhadap kebenaran.</p>
                                    <p>Profesi jurnalis menuntut tanggung jawab moral yang tinggi. Dalam setiap berita yang ditulis, wartawan berperan menjaga transparansi kekuasaan, mengingatkan masyarakat, dan menjadi penjaga nurani publik.</p>
                                    <hr>
