                                    <img src="<?= base_url('assets/img/logo-long.png') ?>" alt="Produk Jurnalistik" class="img-fluid rounded mb-4" style="max-height:260px;">
                                    <p>Ketika mendengar istilah <i>produk jurnalistik</i>, kebanyakan orang langsung membayangkan surat kabar atau berita di televisi. Padahal, dunia jurnalistik jauh lebih luas daripada sekadar laporan harian. Menurut <b>Haris Sumandiria</b> dalam buku <i>Jurnalistik Indonesia</i>, produk jurnalistik mencakup surat kabar, tabloid, majalah, buletin, serta media elektronik seperti radio, televisi, video berita, hingga media daring atau situs web. Semua bentuk ini memiliki satu tujuan utama: menyampaikan informasi yang faktual dan bermakna kepada publik.</p>
                                    <hr>
                                    <h2 class="mt-4">Tiga Kelompok Utama Produk Jurnalistik</h2>
                                    <p>Produk jurnalistik dibagi menjadi tiga kelompok besar: <b>berita</b>, <b>opini</b>, dan <b>iklan</b>.<br>Namun, dari ketiganya hanya dua yang termasuk kategori murni jurnalistik, yaitu berita dan opini. Iklan memang menggunakan teknik penulisan yang serupa dengan karya jurnalistik, tetapi tujuan utamanya bersifat komersial, bukan informatif atau edukatif.</p>
                                    <p>Berita dan opini sama-sama berperan penting dalam menjaga keseimbangan informasi di masyarakat. Berita menyampaikan fakta, sementara opini menafsirkan, mengulas, dan mengajak pembaca berpikir lebih kritis terhadap suatu peristiwa.</p>
                                    <hr>
                                    <h2 class="mt-4">Jenis-Jenis Berita dalam Dunia Jurnalistik</h2>
                                    <ul>
                                        <li><b>Berita langsung (straight news):</b> laporan cepat mengenai peristiwa aktual.</li>
                                        <li><b>Berita khas (feature news):</b> berita dengan sentuhan naratif dan human interest.</li>
                                        <li><b>Berita menyeluruh (comprehensive news):</b> menggabungkan berbagai sudut pandang agar pembaca memahami peristiwa secara utuh.</li>
                                        <li><b>Berita mendalam (depth news)</b> dan <b>pelaporan mendalam (depth reporting):</b> menyajikan penelusuran lebih jauh terhadap suatu isu.</li>
                                        <li><b>Berita penyelidikan (investigative news):</b> hasil riset dan investigasi terhadap kasus penting yang tersembunyi dari publik.</li>
                                        <li><b>Berita gambar (photo news):</b> visualisasi peristiwa dalam bentuk foto jurnalistik yang mampu berbicara lebih kuat daripada kata-kata.</li>
                                    </ul>
                                    <p>Ragam berita ini membuktikan bahwa jurnalisme bukan hanya tentang kecepatan, tetapi juga tentang kedalaman dan ketepatan dalam menyampaikan fakta.</p>
                                    <hr>
                                    <h2 class="mt-4">Kelompok Opini: Suara, Pandangan, dan Refleksi</h2>
                                    <p>Selain berita, karya opini juga menjadi bagian penting dari dunia jurnalistik. Tulisan opini memungkinkan media dan individu menyampaikan pandangan terhadap isu publik. Berikut jenis-jenis karya opini yang sering dijumpai:</p>
                                    <h4 class="mt-3">1. Tajuk Rencana</h4>
                                    <p>Tulisan resmi dari redaksi yang mewakili sikap media terhadap isu aktual atau kontroversial di masyarakat. Tajuk rencana menunjukkan arah pandang media terhadap masalah publik.</p>
                                    <h4 class="mt-3">2. Karikatur</h4>
                                    <p>Karya visual yang berasal dari kata Italia <i>caricare</i> (melebih-lebihkan) dan <i>carattere</i> (wajah). Karikatur menggabungkan humor, kritik, dan pesan sosial secara halus. Karikatur yang baik mampu membuat pembaca tersenyum, sekaligus berpikir.</p>
                                    <h4 class="mt-3">3. Pojok</h4>
                                    <p>Bagian kecil di surat kabar berisi kutipan atau pernyataan singkat yang menggelitik, sering kali disertai komentar tajam dari redaksi. Tujuannya mengingatkan, menegur, atau menyentil fenomena sosial tertentu secara reflektif.</p>
                                    <h4 class="mt-3">4. Artikel</h4>
                                    <p>Tulisan lepas yang berisi opini penulis terhadap persoalan aktual. Artikel bisa bersifat informatif, argumentatif, atau persuasif. Berdasarkan bentuknya, artikel dapat berupa artikel praktis, artikel analisis ahli, artikel ringan, dan artikel halaman opini.</p>
                                    <h4 class="mt-3">5. Kolom</h4>
                                    <p>Tulisan opini yang biasanya muncul secara rutin di media, ditulis oleh seseorang yang memiliki keahlian atau pandangan khas. Kolom berisi pengamatan dan interpretasi penulis terhadap fenomena sosial.</p>
                                    <h4 class="mt-3">6. Surat Pembaca</h4>
                                    <p>Ruang bagi masyarakat untuk menyampaikan pendapat atau kritik terhadap layanan publik, kebijakan, atau peristiwa tertentu. Meski singkat, surat pembaca mencerminkan partisipasi masyarakat dalam mengawasi dan menilai kinerja lembaga atau pemerintah.</p>
                                    <hr>
                                    <h2 class="mt-4">Mengapa Produk Jurnalistik Itu Penting</h2>
                                    <p>Produk jurnalistik adalah hasil akhir dari proses kerja yang panjang—mulai dari peliputan, verifikasi, penulisan, hingga penyajian. Setiap jenis produk memiliki fungsi sosialnya sendiri: berita memberikan fakta, opini memberi tafsir, sementara iklan menjadi penggerak ekonomi media.</p>
                                    <p>Media yang sehat mampu menjaga keseimbangan antara ketiganya. Ketika berita disajikan dengan akurat, opini disampaikan dengan etika, dan iklan ditempatkan secara proporsional, maka kepercayaan publik terhadap media akan tumbuh dengan sendirinya.</p>
                                    <hr>
                                    <h2 class="mt-4">Penutup</h2>
                                    <p>Di tengah derasnya arus informasi digital, memahami jenis-jenis produk jurnalistik membantu pembaca menjadi lebih cerdas dalam memilah informasi. Berita yang baik bukan sekadar cepat, tetapi harus benar dan bermakna. Opini yang baik bukan sekadar berbeda, tetapi harus membangun wawasan.</p>
                                    <p>Jurnalisme sejati hadir untuk menjaga nurani masyarakat—menyampaikan kebenaran, mengingatkan yang lalai, dan menghibur tanpa kehilangan integritas.</p>
